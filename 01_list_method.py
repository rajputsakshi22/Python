l1 = [2,4,3,67,8,90,1]  # list index always start from 0
print(l1)
l1.sort() #to sort the list
print(l1)
l1.reverse()# To reverse the list
print(l1)
l1.insert(3,100) # To insert 100 at index 3
print(l1)
l1.append(99) # To adds 99 at the end
print(l1)
l1.pop(3) # To delete the index 3rd element
print(l1)
l1.remove(99) # To remove the the element
print(l1)